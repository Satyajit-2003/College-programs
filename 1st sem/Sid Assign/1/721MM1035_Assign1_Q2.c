#include<stdio.h>

int main()
{
    int x,y;
    printf("Enter X: ");
    scanf("%d", &x);
    printf("Enter Y: ");
    scanf("%d", &y);

    printf("X=  %d\nY = %d\n", x,y);
    printf("Sum = %d\n",x+y);
    printf("Difference = %d\n",x-y);
    printf("Product = %d\n",x*y);
    printf("Divison = %f\n",(float)x/y);
    
    return 0;
}
