#include<stdio.h>

int main(){
    unsigned int hex, oct, bin;

    printf("Enter a binary number: ");
    scanf("%d", &bin);
    printf("Enter an octal number: ");
    scanf("%o", &oct);
    printf("Enter a hexadecimal number: ");
    scanf("%x", &hex);

    printf("The binary number is %d\n", bin);
    printf("The octal number is %o\n", oct);
    printf("The hexadecimal number is %x\n", hex);

}

