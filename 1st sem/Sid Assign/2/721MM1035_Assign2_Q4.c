#include<stdio.h>

int main(){
    int num1, num2;
    printf("Enter number 1: ");
    scanf("%d",&num1);
    printf("Enter number 2: ");
    scanf("%d",&num2);
    

    printf("Bitwise AND: %d\n", num1&num2);
    printf("Bitwise OR: %d\n", num1|num2);
    printf("Bitwise XOR: %d\n", num1^num2);

    return 0;
}
